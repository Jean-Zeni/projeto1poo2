    public boolean verificarCampos() {
        if (txtNome.equals("")) {
            JOptionPane.showMessageDialog(null, "O campo 'Nome' est√° vazio!");
            return false;
        }

        if (!txtNome.getText().matches("^[\\p{L} ]+$")) {
            JOptionPane.showMessageDialog(null, "O campo 'Nome' possui caracteres inv√°lidos!");
            return false;
        }

        if (txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo'Email' est√° vazio!");
            return false;
        }

        if (!txtEmail.getText().matches(
                "^[a-z0-9._-]+@[a-z._]+.[a-z._]+$")) {
            JOptionPane.showMessageDialog(null, "O campo 'Email' possui caracteres inv√°lidos!");
        }

        if (!txtDataNasc.getText().matches(
                "^[0-9]{2}/[0-9]{2}/[0-9]{4}$")) {
            JOptionPane.showMessageDialog(null, "O campo 'Data de nascimento' possui formata√ß√£o incorreta"
                    + " Ex.: 01/01/1990");
            return false;
        }

        String senha = new String(txtSenha.getPassword());
        String confSenha = new String(txtConfSenha.getPassword());

        if (senha.length() < 8) {
            JOptionPane.showMessageDialog(null, "O campo 'Senha' est√° vazio ou cont√©m menos de 8 caracteres!");
            return false;
        }

        if (!senha.equals(confSenha)) {
            JOptionPane.showMessageDialog(null, "A confirma√ß√£o e a senha necessitam ser iguais!");
            return false;
        }

        return true;
    }

    public void gravar() {
        //INSTANCIAR UM OBJETO USUARIO
        Usuario usu = new Usuario();

        usu.setNome(txtNome.getText());
        usu.setEmail(txtEmail.getText());

        String senha = new String(txtSenha.getPassword());
        String senhaHash = Utils.calcularHash(senha);
        usu.setSenha(senhaHash);

        Date data = Utils.converterStringToDate(txtDataNasc.getText());
        usu.setDataNasc(data);

        usu.setAtivo(cbAtivo.isSelected());

        UsuarioController controller = new UsuarioController();

        if (controller.inserirUsuario(usu)) {
            JOptionPane.showMessageDialog(null, "Usu√°rio: " + usu.getNome() + " inserido com sucesso!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Usu√°rio n√£o pode ser inserido!");
        }
    }

