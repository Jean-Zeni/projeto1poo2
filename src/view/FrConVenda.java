        private void pesquisar() {

        DefaultTableModel modelo = (DefaultTableModel) tbConVend.getModel();

        modelo.setNumRows(0);

        VendasController controller = new VendasController();

        int tipoFiltro = cbFiltro.getSelectedIndex();
        String filtro = txtFiltro.getText();

        List<Venda> listaVendas = controller.buscarVendas(tipoFiltro, filtro);

        for (Venda venda : listaVendas) {
            Object[] linha = {
                venda.getPkVenda(),
                venda.getDataVenda(),
                venda.getValorTotal(),
                venda.getCpfComprador(),
                venda.getNomeComprador()
            };

            modelo.addRow(linha);
        }
        
    }
    
